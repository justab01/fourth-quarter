export function getEspnAthleteId(name: string, league: string): string | undefined {
  switch (league) {
    case "NBA": return ESPN_NBA_IDS[name];
    case "NFL": return ESPN_NFL_IDS[name];
    case "MLB": return ESPN_MLB_IDS[name];
    case "MLS": return ESPN_MLS_IDS[name];
    default: return undefined;
  }
}

const HEADSHOT_SPORT: Record<string, string> = {
  NBA: "nba", NFL: "nfl", MLB: "mlb", MLS: "soccer",
};

export function getEspnHeadshotUrl(name: string, league: string): string | null {
  const id = getEspnAthleteId(name, league);
  if (!id) return null;
  const sport = HEADSHOT_SPORT[league];
  if (!sport) return null;
  return `https://a.espncdn.com/combiner/i?img=/i/headshots/${sport}/players/full/${id}.png`;
}

export function getEspnStatsUrl(name: string, league: string): string | null {
  const id = getEspnAthleteId(name, league);
  if (!id) return null;
  const leagueConfig: Record<string, { sport: string; league: string }> = {
    NBA: { sport: "basketball", league: "nba" },
    NFL: { sport: "football", league: "nfl" },
    MLB: { sport: "baseball", league: "mlb" },
    MLS: { sport: "soccer", league: "usa.1" },
  };
  const cfg = leagueConfig[league];
  if (!cfg) return null;
  // Using /stats on site.web.api.espn.com (not /statistics on site.api.espn.com) because
  // /statistics returns a 404 for most athletes while /stats reliably returns per-season averages.
  return `https://site.web.api.espn.com/apis/common/v3/sports/${cfg.sport}/${cfg.league}/athletes/${id}/stats`;
}

export function getEspnGamelogUrl(name: string, league: string, season?: string): string | null {
  const id = getEspnAthleteId(name, league);
  if (!id) return null;
  // Season year = ending year of season
  // NBA 2025-26 = 2026, NFL 2025 season = 2025, MLB 2025 = 2025, MLS 2026 = 2026
  const leagueConfig: Record<string, { sport: string; league: string; defaultSeason: string }> = {
    NBA: { sport: "basketball", league: "nba", defaultSeason: "2026" },
    NFL: { sport: "football", league: "nfl", defaultSeason: "2025" },
    MLB: { sport: "baseball", league: "mlb", defaultSeason: "2025" },
    MLS: { sport: "soccer", league: "usa.1", defaultSeason: "2026" },
  };
  const cfg = leagueConfig[league];
  if (!cfg) return null;
  const s = season ?? cfg.defaultSeason;
  return `https://site.web.api.espn.com/apis/common/v3/sports/${cfg.sport}/${cfg.league}/athletes/${id}/gamelog?season=${s}`;
}
